export const preloadedRawMaterials: { name: string; quantity: number }[] = rawMaterialsListString
  .split('\n')
  .map(line => {
    const parts = line.trim().split('\t');
    if (parts.length < 2) return null;
    const name = parts[0].trim();
    const quantity = parseFloat(parts[1]) || 0;
    return { name, quantity };
  })
  .filter((item): item is { name: string; quantity: number } => item !== null && !!item.name);


export const getCategoryFromName = (name: string): string => {
  const lowerName = name.toLowerCase();
  if (lowerName.includes("agarbatti") || lowerName.includes("dhoop") || lowerName.includes("sambrani") || lowerName.includes("cup")) return "Incense & Dhoop";
  if (lowerName.includes("powder") || lowerName.includes("chilli") || lowerName.includes("haldi") || lowerName.includes("rai") || lowerName.includes("methi") || lowerName.includes("jira") || lowerName.includes("dhaniya")) return "Spices & Powders";
  if (lowerName.includes("ghee")) return "Ghee & Dairy";
  if (lowerName.includes("oil")) return "Oils";
  if (lowerName.includes("soap") || lowerName.includes("ubtan") || lowerName.includes("shama") || lowerName.includes("faceglow")) return "Personal Care";
  if (lowerName.includes("chunda")) return "Pickles & Preserves";
  if (lowerName.includes("go-ark") || lowerName.includes("gomutra") || lowerName.includes("ghanavati") || lowerName.includes("triphla") || lowerName.includes("dantaraksha")) return "Ayurvedic Products";
  if (lowerName.includes("tea")) return "Beverages";
  return "General";
};

